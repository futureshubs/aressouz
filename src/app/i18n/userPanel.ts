const DICT: Record<Language, Record<string, string>> = { uz: UZ, ru: RU, en: EN };

export function userPanelT(lang: Language, key: string, vars?: Record<string, string | number>): string {
  let s = DICT[lang]?.[key] ?? DICT.uz[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

export function useUserPanelT() {
  const { language } = useTheme();
  return (key: string, vars?: Record<string, string | number>) => userPanelT(language, key, vars);
}

export function userPanelLocale(lang: Language): string {
  if (lang === 'ru') return 'ru-RU';
  if (lang === 'en') return 'en-GB';
  return 'uz-UZ';
}

export function userPanelFormatDateTime(lang: Language, d: Date): string {
  return d.toLocaleString(userPanelLocale(lang), { dateStyle: 'medium', timeStyle: 'short' });
}

export function profilePaymentStatus(lang: Language, raw: string): string {
  const s = String(raw || '').toLowerCase().trim();
  if (['paid', 'completed'].includes(s)) return userPanelT(lang, 'profile.pay.paid');
  if (['pending', 'initiated', 'authorized'].includes(s)) return userPanelT(lang, 'profile.pay.pending');
  if (['failed'].includes(s)) return userPanelT(lang, 'profile.pay.failed');
  if (['refunded', 'returned'].includes(s)) return userPanelT(lang, 'profile.pay.refunded');
  if (['cancelled', 'canceled'].includes(s)) return userPanelT(lang, 'profile.pay.cancelled');
  return s || userPanelT(lang, 'profile.pay.unknown');
}

export function profileCategoryLabel(lang: Language, categoryKey: string): string {
  const k = String(categoryKey || 'market');
  const map: Record<string, string> = {
    market: 'profile.catMarket',
    shop: 'profile.catShop',
    food: 'profile.catFood',
    rent: 'profile.catRent',
    auction: 'profile.catAuction',
  };
  return userPanelT(lang, map[k] || 'profile.catMarket');
}

export function profileOrderBadgeLabel(
  lang: Language,
  order: { orderStatus?: string; awaitingCustomerReceipt?: boolean },
): string {
  if (order.awaitingCustomerReceipt) return userPanelT(lang, 'profile.order.awaitingReceipt');
  const os = String(order.orderStatus || '');
  if (os === 'completed') return userPanelT(lang, 'profile.order.completed');
  if (os === 'cancelled') return userPanelT(lang, 'profile.order.cancelled');
  return userPanelT(lang, 'profile.order.active');
}

export function profileAccentLabel(lang: Language, accentId: string): string {
  const id = String(accentId || 'teal').toLowerCase();
  return userPanelT(lang, `settings.accent.${id}`);
}

export function formatRentalCountdownLine(
  lang: Language,
  overdue: boolean,
  days: number,
  hours: number,
  mins: number,
): string {
  if (overdue) {
    if (days > 0) return userPanelT(lang, 'rental.lateDays', { d: days, h: hours });
    if (hours > 0) return userPanelT(lang, 'rental.lateHours', { h: hours, m: mins });
    return userPanelT(lang, 'rental.lateMins', { m: mins });
  }
  if (days > 0) return userPanelT(lang, 'rental.leftDays', { d: days, h: hours });
  if (hours > 0) return userPanelT(lang, 'rental.leftHours', { h: hours, m: mins });
  return userPanelT(lang, 'rental.leftMins', { m: mins });
}

export function nextInstallmentAmountI18n(
  lang: Language,
  pricePerPeriod: number | undefined,
  quantity: number | undefined,
): string | null {
  const p = Number(pricePerPeriod);
  const q = Math.max(1, Number(quantity) || 1);
  if (!Number.isFinite(p) || p <= 0) return null;
  const loc = userPanelLocale(lang);
  const cur = userPanelT(lang, 'profile.currency');
  return `${Math.round(p * q).toLocaleString(loc)} ${cur}`;
}
